#4. Write a R Program to Sample data from a Population.
set.seed(123)

population <- 1:1000

sample_size <- 50

sampled_data <- sample(population, size = sample_size, replace = FALSE)

cat("Original Population (first 20 elements):\n")
print(head(population, 20))
cat("\nTotal population size:", length(population), "\n\n")

cat("Randomly Sampled Data:\n")
print(sampled_data)
cat("\nSample size:", length(sampled_data), "\n")
